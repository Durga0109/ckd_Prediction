from fpdf import FPDF
import pandas as pd
from datetime import datetime

def sanitize_text(text):
    """Replace Unicode characters that helvetica can't render with ASCII equivalents."""
    replacements = {
        '\u2013': '-',   # en-dash
        '\u2014': '-',   # em-dash
        '\u2018': "'",   # left single quote
        '\u2019': "'",   # right single quote
        '\u201c': '"',   # left double quote
        '\u201d': '"',   # right double quote
        '\u2026': '...', # ellipsis
        '\u00b5': 'u',   # micro sign
        '\u00b2': '2',   # superscript 2
        '\u2265': '>=',  # greater than or equal
        '\u2264': '<=',  # less than or equal
    }
    for char, replacement in replacements.items():
        text = text.replace(char, replacement)
    return text

# Human-readable names for raw feature codes
FEATURE_LABELS = {
    'sc': 'Serum Creatinine', 'hemo': 'Hemoglobin', 'pcv': 'Packed Cell Volume',
    'bp': 'Blood Pressure', 'bgr': 'Blood Glucose (Random)', 'bu': 'Blood Urea',
    'al': 'Urinary Albumin', 'sg': 'Specific Gravity', 'sod': 'Sodium',
    'pot': 'Potassium', 'wbcc': 'WBC Count', 'rbcc': 'RBC Count',
    'htn': 'Hypertension', 'dm': 'Diabetes Mellitus', 'ane': 'Anemia',
    'age': 'Age', 'sex': 'Sex', 'su': 'Sugar', 'rbc': 'Red Blood Cells (Urine)',
    'pc': 'Pus Cell', 'pcc': 'Pus Cell Clumps', 'ba': 'Bacteria',
    'cad': 'Coronary Artery Disease', 'appet': 'Appetite', 'pe': 'Pedal Edema',
}

class PatientReportPDF(FPDF):
    def header(self):
        self.set_font('helvetica', 'B', 16)
        self.cell(0, 10, 'CKD Clinical Decision Support Report', 0, 1, 'C')
        self.set_font('helvetica', '', 9)
        self.set_text_color(100, 100, 100)
        self.cell(0, 5, 'Diagnostic Analysis', 0, 1, 'C')
        self.set_text_color(0, 0, 0)
        self.line(10, self.get_y() + 2, 200, self.get_y() + 2)
        self.ln(5)

    def footer(self):
        self.set_y(-20)
        self.set_font('helvetica', 'I', 7)
        self.set_text_color(120, 120, 120)
        self.multi_cell(0, 4, 
            'DISCLAIMER: This report is generated by an AI-assisted clinical decision support system and is intended '
            'for use as a supplementary tool only. It does not constitute a definitive medical diagnosis. '
            'All clinical decisions must be validated by a qualified healthcare professional.',
            align='C')
        self.cell(0, 4, f'Page {self.page_no()}', 0, 0, 'C')
        self.set_text_color(0, 0, 0)

    def section_header(self, title):
        """Render a styled section header"""
        self.set_fill_color(41, 65, 122)  # Clinical blue
        self.set_text_color(255, 255, 255)
        self.set_font('helvetica', 'B', 11)
        self.cell(0, 9, f'  {title}', 0, 1, 'L', True)
        self.set_text_color(0, 0, 0)
        self.ln(2)

    def key_value_row(self, label, value, bold_value=False):
        """Render a label-value row"""
        self.set_font('helvetica', '', 10)
        self.cell(55, 7, label, 0)
        self.set_font('helvetica', 'B' if bold_value else '', 10)
        self.cell(0, 7, str(value), 0, 1)


def generate_patient_pdf(patient_name, prediction_data):
    """
    Generate a comprehensive clinical PDF report from prediction data.
    """
    pdf = PatientReportPDF()
    pdf.add_page()
    
    # ========== 1. PATIENT & VISIT INFO ==========
    pdf.section_header("Patient Information")
    pdf.key_value_row("Patient Name:", patient_name, bold_value=True)
    visit_date_raw = prediction_data.get('visit_date', prediction_data.get('created_at', 'N/A'))
    pdf.key_value_row("Visit Date:", str(visit_date_raw)[:10])
    pdf.ln(3)

    # ========== 2. DIAGNOSTIC SUMMARY ==========
    pdf.section_header("Diagnostic Summary")
    
    pred = prediction_data['ckd_prediction']
    prob = prediction_data['ckd_probability'] if pred == "CKD" else prediction_data['no_ckd_probability']
    
    pdf.key_value_row("CKD Prediction:", pred, bold_value=True)
    pdf.key_value_row("Statistical Probability:", f"{prob*100:.1f}%")
    pdf.key_value_row("Confidence Level:", prediction_data.get('confidence_level', 'N/A'))
    pdf.key_value_row("Risk Level:", prediction_data.get('risk_level', 'N/A'))
    pdf.key_value_row("eGFR:", f"{prediction_data.get('egfr', 'N/A')} mL/min")
    pdf.key_value_row("CKD Stage:", f"Stage {prediction_data.get('ckd_stage', 'N/A')}")
    stage_desc = prediction_data.get('stage_description', '')
    if stage_desc:
        pdf.set_font('helvetica', 'I', 9)
        pdf.cell(55, 6, '')
        pdf.cell(0, 6, sanitize_text(stage_desc), 0, 1)
    pdf.ln(3)

    # ========== 3. CLINICAL REASONING (XAI Narrative) ==========
    narrative = prediction_data.get('xai_narrative', {})
    if narrative:
        pdf.section_header("Clinical Reasoning")
        
        # Summary
        pdf.set_font('helvetica', '', 10)
        pdf.multi_cell(0, 6, sanitize_text(narrative.get('summary', '')))
        pdf.ln(3)
        
        # Risk Drivers
        drivers = narrative.get('risk_drivers', [])
        if drivers:
            pdf.set_font('helvetica', 'B', 10)
            pdf.cell(0, 7, "Key Biomarker Analysis:", 0, 1)
            pdf.set_font('helvetica', '', 9)
            for driver in drivers:
                pdf.set_x(pdf.l_margin)
                pdf.set_font('helvetica', 'B', 9)
                pdf.multi_cell(190, 6, sanitize_text(f"{driver['biomarker']} - {driver['value']}"))
                pdf.set_x(pdf.l_margin)
                pdf.set_font('helvetica', '', 9)
                pdf.set_text_color(80, 80, 80)
                pdf.multi_cell(190, 5, sanitize_text(driver['clinical_context']))
                pdf.set_text_color(0, 0, 0)
                pdf.ln(1)
            pdf.ln(2)
        
        # Agreement
        agreement = narrative.get('agreement', {})
        if agreement.get('text'):
            pdf.set_font('helvetica', 'B', 10)
            pdf.cell(0, 7, "SHAP-LIME Cross-Validation:", 0, 1)
            pdf.set_font('helvetica', '', 9)
            pdf.multi_cell(0, 5, sanitize_text(agreement['text']))
        pdf.ln(3)

    # ========== 4. SHAP FEATURE TABLE ==========
    pdf.section_header("Top Risk Factors (SHAP Analysis)")
    
    pdf.set_fill_color(230, 230, 230)
    pdf.set_font('helvetica', 'B', 9)
    pdf.cell(70, 7, "Biomarker", 1, 0, 'C', True)
    pdf.cell(35, 7, "Direction", 1, 0, 'C', True)
    pdf.cell(35, 7, "SHAP Value", 1, 0, 'C', True)
    pdf.cell(0, 7, "Raw Code", 1, 1, 'C', True)

    pdf.set_font('helvetica', '', 9)
    top_features = prediction_data.get('top_features', [])[:5]
    for feat in top_features:
        code = feat['feature']
        label = FEATURE_LABELS.get(code, code)
        pdf.cell(70, 7, label, 1)
        pdf.cell(35, 7, feat['impact'].capitalize(), 1, 0, 'C')
        pdf.cell(35, 7, f"{feat['shap_value']:+.4f}", 1, 0, 'C')
        pdf.cell(0, 7, code, 1, 1, 'C')
    pdf.ln(3)

    # ========== 5. LIME SUMMARY ==========
    lime_data = prediction_data.get('lime_values', {})
    lime_feats = lime_data.get('top_features', [])
    if lime_feats:
        pdf.section_header("Local Explanation (LIME Analysis)")
        lime_r2 = lime_data.get('lime_score', 0)
        pdf.key_value_row("Local Fidelity (R² Score):", f"{lime_r2:.2f}")
        pdf.ln(2)
        
        pdf.set_fill_color(230, 230, 230)
        pdf.set_font('helvetica', 'B', 9)
        pdf.cell(70, 7, "Biomarker", 1, 0, 'C', True)
        pdf.cell(35, 7, "Direction", 1, 0, 'C', True)
        pdf.cell(0, 7, "LIME Weight", 1, 1, 'C', True)
        
        pdf.set_font('helvetica', '', 9)
        sorted_lime = sorted(lime_feats, key=lambda x: abs(x['lime_weight']), reverse=True)[:5]
        for feat in sorted_lime:
            code = feat['feature']
            label = FEATURE_LABELS.get(code, code)
            pdf.cell(70, 7, label, 1)
            pdf.cell(35, 7, feat['impact'].capitalize(), 1, 0, 'C')
            pdf.cell(0, 7, f"{feat['lime_weight']:+.6f}", 1, 1, 'C')
        pdf.ln(3)

    # ========== 6. CLINICAL RECOMMENDATIONS ==========
    recs = prediction_data.get('recommendations', [])
    if recs:
        pdf.section_header("Clinical Recommendations")
        pdf.set_font('helvetica', '', 9)
        for rec in recs:
            # Strip markdown and emoji
            clean_rec = rec
            for emoji in ["💓 ", "🩸 ", "⚠️ ", "🛡️ ", "🍭 ", "🍌 ", "📉 ", "💧 ", "🦠 ", "✅ "]:
                clean_rec = clean_rec.replace(emoji, "")
            clean_rec = clean_rec.replace("**", "")
            pdf.multi_cell(0, 6, sanitize_text(f"- {clean_rec}"))
            pdf.ln(1)
        pdf.ln(2)

    # ========== 7. INPUT DATA SNAPSHOT ==========
    input_data = prediction_data.get('input_data', {})
    if input_data:
        pdf.section_header("Laboratory Values (Input Data Snapshot)")
        
        pdf.set_fill_color(230, 230, 230)
        pdf.set_font('helvetica', 'B', 9)
        pdf.cell(60, 7, "Parameter", 1, 0, 'C', True)
        pdf.cell(30, 7, "Value", 1, 0, 'C', True)
        pdf.cell(60, 7, "Parameter", 1, 0, 'C', True)
        pdf.cell(0, 7, "Value", 1, 1, 'C', True)
        
        pdf.set_font('helvetica', '', 9)
        items = [(k, v) for k, v in input_data.items() if k != 'sex']
        # Print in 2-column layout
        for i in range(0, len(items), 2):
            k1, v1 = items[i]
            pdf.cell(60, 7, FEATURE_LABELS.get(k1, k1), 1)
            pdf.cell(30, 7, str(v1), 1, 0, 'C')
            if i + 1 < len(items):
                k2, v2 = items[i + 1]
                pdf.cell(60, 7, FEATURE_LABELS.get(k2, k2), 1)
                pdf.cell(0, 7, str(v2), 1, 1, 'C')
            else:
                pdf.cell(60, 7, '', 1)
                pdf.cell(0, 7, '', 1, 1, 'C')

    return bytes(pdf.output())
